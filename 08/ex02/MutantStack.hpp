#ifndef MUTANTSTACK_HPP
#define MUTANTSTACK_HPP

#include <stack>       // Для использования std::stack
#include <iterator>    // Для работы с итераторами

// Шаблонный класс MutantStack, расширяющий std::stack
template <typename T>
class MutantStack : public std::stack<T> {
public:
    // Конструктор по умолчанию
    MutantStack() : std::stack<T>() {}

    // Конструктор копирования
    MutantStack(const MutantStack &other) : std::stack<T>(other) {}

    // Деструктор
    ~MutantStack() {}

    // Оператор присваивания копированием
    MutantStack &operator=(const MutantStack &other) {
        if (this != &other) {
            this->c = other.c; // Прямое копирование контейнера
        }
        return *this;
    }
    

    // Тип итератора для MutantStack
    typedef typename std::stack<T>::container_type::iterator iterator;

    // Функция для получения итератора на начало
    iterator begin() {
        return this->c.begin();  // Возвращаем итератор на начало контейнера
    }

    // Функция для получения итератора на конец
    iterator end() {
        return this->c.end();    // Возвращаем итератор на конец контейнера
    }
};

#endif

/*std::stack не предоставляет доступа к своему контейнеру 
напрямую. Это сделано, чтобы скрыть детали реализации (например, 
использовать std::deque или std::vector для хранения данных).
В классе MutantStack мы добавляем возможность работы с итераторами, обращаясь к внутреннему контейнеру через this->c. Это дает возможность перебирать элементы стека так, как если бы это был обычный 
контейнер с итераторами, например, std::vector или std::list.
this->c — это обращение к внутреннему контейнеру стека, в котором хранится его содержимое.

Этот контейнер хранит элементы, и благодаря этому мы можем использовать методы, такие как begin() и end(), чтобы работать с ними, даже если класс std::stack не 
предоставляет прямого доступа к своему контейнеру.
std::stack скрывает детали реализации. Вы не знаете, используется ли в данном случае std::deque, std::vector или другой контейнер, и не можете напрямую работать с этим контейнером.

Например, в стеке не существует методов begin() или end(), которые бы позволяли вам перебирать все элементы в контейнере. Все операции выполняются только с верхним элементом стека.*/
